package com.example.proj.game.DifficultyLevels;

public enum GeneralDifficulty {
    easy,   // 6x6 map with 1 lightbulb and easy map generation
    easy8x8, // 8x8 map with same rules
    medium, // 8x8 map with 1-3 lightbulbs and resourceful map
    medium10x10, // 10x10 map with same rules
    medium12x12, // 12x12 map with same rules
    medium14x14, // 14x14 map with same rules
    hard, // 10x10 map with 2-5 lightbulbs and unforgiving map resources
    hard12x12, // 12x12 map with same rules
    hard14x14, // 14x14 map with same rules
    hard16x16, // 16x16 map with same rules
}
